// Declarando uma variável local com o método VAR(Recomendado)
var variable_local = 50;
console.log(variable_local)

// Declarando uma variável de forma global(Caso tenha uma mesma variável com nome igual o seu valor será alterado)
variable_global = "Estudando JavaScript";
console.log(variable_global)

// Declarando variável com o método CONST
const name = "Kesley"
console.log(name)

// Declarando variável com o método LET
let number = 10;
console.log(number)

// Declarando uma variável vázia com o método VAR que terá o seu valor no decorrer da construção do código
var fruit;
fruit = "Banana"
console.log(fruit)




