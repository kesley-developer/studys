// Operadores de atribuição

// += (Soma)
console.log("Sum");
var number0 = 0;
while(number0 <= 10){
    console.log(number0);

    number0 += 1; // Somando a variável por 1 a cada loop
}

// -= (Subtração)
console.log("Subtration");
var number1 = 10;
while(number1 >= 0){
    console.log(number1);

    number1 -= 1; // Subtraindo a variável por 1 a cada loop
}

// *= (Multiplicação)
console.log("Multiplication");
var number2 = 1;
while(number2 <= 20){
    console.log(number2);

    number2 *= 2 // Multiplicando a variável por 2 a cada loop
}

// /= (Divisão)
console.log("Division");
var number3 = 8;
while(number3 <= 2){
    console.log(number3);

    number3 /= 2 // Dividindo a variável por 2 a cada loop
}
