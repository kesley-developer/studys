// Comparação de tipo e valor


// Comparando NUMBERS
var my_number = 10;
if(my_number === 10){ // Caso o tipo de dado da variável sejá igual a number
    console.log("The value is number");
}

else if(my_number !== 10){ // Caso o tipo de dado for diferente de number
    console.log("The value is string");
}

// Comparando STRINGS
var name = "kesley";
if(name === "kesley"){ // Caso o tipo de dado da variável seja igual a string
    console.log("The value is string");
}

else if(name !== "kesley"){ // Caso o tipo de dado for diferente de string e for number
    console.log("The value is number");
}