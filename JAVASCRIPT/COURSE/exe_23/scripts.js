// querySelector
var items_query = document.querySelector("#list-1 li");
console.log(items_query);

// querySelectorAll
var items_queryAll = document.querySelectorAll("#list-2 li");
console.log(items_queryAll);

/* 
*  A diferença entre os dois seletores
*  é a seguinte: o querySelector seleciona
*  apenas o primeiro elemento que encontrar, 
*  diferente do querySelectorAll que seleciona
*  diversos elementos
*/
